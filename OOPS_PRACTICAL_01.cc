#include<iostream>

using namespace std;
int main(){
    int num1,num2;
    cout<<"ENTER NUM1,NUM2-";
    cin>>num1>>num2;
    //ARITHMETIC OPERATION INCLUDES SUM,SUB,MUL,DIV,MODULO(that gives remainder)
    cout<<"SUM="<<num1+num2<<endl;
    cout<<"SUB="<<num1+num2<<endl;
    cout<<"MUL="<<num1*num2<<endl;
    cout<<"DIV="<<num1/num2<<endl;
    cout<<"MOD="<<num1%num2<<endl;
    return 0;
}